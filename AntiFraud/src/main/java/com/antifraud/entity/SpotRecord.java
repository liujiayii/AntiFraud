package com.antifraud.entity;

import java.util.Date;

/**
 * 
 *
 * @ClassName: SpotRecord

 * @description 实地记录表实体类
 *
 * @author LiuHangjing
 * @createDate 2018年10月22日下午5:44:04
 */
public class SpotRecord {
	/** 主键 */
	private Long id;
	
	/** 报单编号 */
	private String report_id;
	
	/** 实地人员id */
	private Long user_id;
	 //终端id
    private long tid;
    //服务id
    private long sid;
    //轨迹id
    private long trid;
	
	/** 实地时间 */
	private Date spot_time; 
	
	/** 详细地址 */	
	private String address;
	
	/** 备注原因 */
	private String remark;
	
	/** 创建时间 */
	private Date create_time; 
	
	/** 修改时间 */
	private Date update_time;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getReport_id() {
		return report_id;
	}

	public void setReport_id(String report_id) {
		this.report_id = report_id;
	}

	public Long getUser_id() {
		return user_id;
	}

	public void setUser_id(Long user_id) {
		this.user_id = user_id;
	}

	public Date getSpot_time() {
		return spot_time;
	}

	public void setSpot_time(Date spot_time) {
		this.spot_time = spot_time;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Date getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

	public Date getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(Date update_time) {
		this.update_time = update_time;
	}

	public long getTid() {
		return tid;
	}

	public void setTid(long tid) {
		this.tid = tid;
	}

	public long getSid() {
		return sid;
	}

	public void setSid(long sid) {
		this.sid = sid;
	}

	public long getTrid() {
		return trid;
	}

	public void setTrid(long trid) {
		this.trid = trid;
	}

	@Override
	public String toString() {
		return "SpotRecord [id=" + id + ", report_id=" + report_id + ", user_id=" + user_id + ", tid=" + tid + ", sid="
				+ sid + ", trid=" + trid + ", spot_time=" + spot_time + ", address=" + address + ", remark=" + remark
				+ ", create_time=" + create_time + ", update_time=" + update_time + "]";
	}

	
	
}	
