package com.antifraud.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * 
 *
 * @ClassName: UserRole
 * @description 用户角色表
 *
 * @author LiuHangjing
 * @createDate 2018年9月26日下午4:18:26
 */
public class UserRole implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/** 编号*/
	private Long id;
	/** 用户表id*/
	private Long user_id;
	/** 角色表id*/
	private Long role_id;
	/** 创建时间*/
	private Date create_time;
	/** 修改时间*/
	private Date update_time;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getUser_id() {
		return user_id;
	}

	public void setUser_id(Long user_id) {
		this.user_id = user_id;
	}

	public Long getRole_id() {
		return role_id;
	}

	public void setRole_id(Long role_id) {
		this.role_id = role_id;
	}

	public Date getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

	public Date getUpdater_time() {
		return update_time;
	}

	public void setUpdater_time(Date update_time) {
		this.update_time = update_time;
	}

	@Override
	public String toString() {
		return "UserRole [id=" + id + ", user_id=" + user_id + ", role_id=" + role_id + ", create_time=" + create_time
				+ ", updater_time=" + update_time + "]";
	}
	
	
}
